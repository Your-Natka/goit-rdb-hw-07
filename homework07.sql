-- =====================================================================
-- 1. Витягнути рік, місяць і число з атрибута date
-- =====================================================================
SELECT 
    id,
    date,
    YEAR(date) AS year,
    MONTH(date) AS month,
    DAY(date) AS day
FROM orders;

-- =====================================================================
-- 2. Додати один день до атрибута date
-- =====================================================================
SELECT 
    id,
    date,
    DATE_ADD(date, INTERVAL 1 DAY) AS date_plus_one
FROM orders;

-- =====================================================================
-- 3. Показати кількість секунд від початку відліку (timestamp)
-- =====================================================================
SELECT 
    id,
    date,
    UNIX_TIMESTAMP(date) AS seconds_since_epoch
FROM orders;

-- =====================================================================
-- 4. Порахувати рядки з date у певному проміжку
SELECT COUNT(*) AS rows_in_range
FROM orders
WHERE date BETWEEN '1996-07-10 00:00:00' AND '1996-10-08 00:00:00';
-- =====================================================================
-- 5. Вивести JSON-об’єкт для кожного рядка
-- =====================================================================
SELECT 
    id,
    date,
    JSON_OBJECT(
        'id', id,
        'date', date
    ) AS json_data
FROM orders;
-- =====================================================================